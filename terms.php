            <h3>Artículo 1: Definición del Servicio</h3>
            <p>Interpago actúa como un intermediario neutral y agente de depósito en garantía (escrow). Nuestro rol se limita a: (1) Recibir y custodiar los fondos pagados por el Comprador; (2) Notificar al Vendedor que los fondos han sido asegurados; y (3) Liberar dichos fondos al Vendedor (descontando comisiones) únicamente después de que el Comprador confirme la recepción y aceptación del producto o servicio. Interpago no es parte del contrato de compraventa y no garantiza la calidad o legalidad de los productos.</p>

            <h3>Artículo 2: Cuentas de Usuario y Verificación (KYC)</h3>
            <p>Para usar nuestros servicios, debe ser mayor de edad y con capacidad legal. Usted se compromete a proporcionar información real y actualizada. Nos reservamos el derecho de solicitar documentación (fotos de su documento de identidad, selfies) para verificar su identidad (KYC), siendo este un requisito para acceder a todas las funcionalidades, como los retiros de saldo. Usted es el único responsable de la seguridad de su contraseña y cuenta.</p>

            <h3>Artículo 3: Proceso de la Transacción</h3>
            <ul>
                <li><strong>Acuerdo:</strong> Las partes definen los términos (producto, precio, responsable de la comisión).</li>
                <li><strong>Pago en Custodia:</strong> El Comprador paga a Interpago. Los fondos quedan retenidos de forma segura.</li>
                <li><strong>Confirmación de Entrega:</strong> El Vendedor envía el producto. El Comprador es responsable de confirmar la recepción, preferiblemente escaneando el Sello Digital QR incluido en el paquete, que sirve como prueba de entrega.</li>
                <li><strong>Período de Inspección:</strong> El Comprador dispone de 24 horas tras la confirmación de recepción para inspeccionar el producto. Si no hay disputas en este plazo, se considera aceptado.</li>
                <li><strong>Liberación de Fondos:</strong> Tras la aceptación, Interpago acredita el monto correspondiente a la billetera virtual del Vendedor.</li>
            </ul>

            <h3>Artículo 4: Política de Disputas</h3>
            <p>Si el producto no corresponde a lo acordado, el Comprador puede iniciar una disputa. Interpago actuará como mediador neutral, revisando la evidencia de ambas partes (chat, fotos, etc.). La decisión de Interpago será final y podrá resultar en la liberación de fondos, un reembolso total o parcial, u otra solución que consideremos justa.</p>

            <h3>Artículo 5: Usos Prohibidos y Cumplimiento Legal</h3>
            <p>Está estrictamente prohibido usar Interpago para actividades ilegales bajo la ley colombiana, incluyendo lavado de activos o venta de artículos prohibidos. Nos reservamos el derecho de suspender cuentas, retener fondos y reportar actividades sospechosas a la UIAF y otras autoridades competentes.</p>

            <h3>Artículo 6: Limitación de Responsabilidad</h3>
            <p>La responsabilidad de Interpago se limita exclusivamente a la correcta custodia y desembolso de los fondos según estos Términos. No somos responsables por la calidad, seguridad o legalidad de los ítems transaccionados, ni por problemas logísticos fuera de nuestro control directo.</p>
